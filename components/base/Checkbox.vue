<template>
  <div>
    <label for="checkbox" class="container"
      >Send as dropshipper
      <input id="checkbox" ref="checkbox" v-model="input" type="checkbox" />
      <span class="checkmark"></span>
    </label>
  </div>
</template>

<script>
export default {
  props: {
    color: { type: String, default: null },
    value: { type: String, default: null }
  },
  data: () => ({
    input: null
  }),
  watch: {
    value(value) {
      this.input = value
    },
    input(value) {
      this.$emit('input', value)
    }
  },
  created() {
    this.input = this.value
  }
}
</script>

<style lang="stylus" scoped>

